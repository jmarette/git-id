#[test]
fn acceptance_git_id_works_as_native_git_subcommand() {
    let t = TestEnv::new();
    t.setup_work();
    let repo = t.init_repo("dev/work/repo");

    // Put the freshly built binary on PATH and let *git* dispatch to it.
    // Build PATH with the platform separator so `git` finds `git-id`(.exe)
    // as the `id` subcommand on every OS.
    let bin = assert_cmd::cargo::cargo_bin("git-id");
    let mut search = vec![bin.parent().unwrap().to_path_buf()];
    search.extend(std::env::split_paths(
        &std::env::var_os("PATH").unwrap_or_default(),
    ));
    let path = std::env::join_paths(search).unwrap();

    let mut c = std::process::Command::new("git");
    c.env("HOME", &t.home)
        .env("XDG_CONFIG_HOME", t.home.join(".config"))
        .env("GIT_CONFIG_NOSYSTEM", "1")
        .env("LC_ALL", "C")
        .env("PATH", &path)
        .current_dir(&repo)
        .args(["id", "which"]);
    let out = c.output().unwrap();
    assert!(
        out.status.success(),
        "`git id which` failed: {}",
        String::from_utf8_lossy(&out.stderr)
    );
    let stdout = String::from_utf8_lossy(&out.stdout);
    assert!(stdout.contains("work"), "{stdout}");
}


#[test]
fn list_shows_identities_routes_and_json() {
    let t = TestEnv::new();
    let dir = t.setup_work();
    t.ok(&[
        "create",
        "personal",
        "--name",
        "Jane Doe",
        "--email",
        "jane@personal.example",
    ]);

    let out = t.ok(&["list"]);
    assert!(out.contains("work"), "{out}");
    assert!(out.contains("jane@work.example"), "{out}");
    assert!(out.contains("personal"), "{out}");
    assert!(out.contains("(no routes)"), "{out}");

    let out = t.ok(&["list", "--paths"]);
    assert!(out.contains("->  work"), "{out}");

    let json: serde_json::Value = serde_json::from_str(&t.ok(&["list", "--json"])).unwrap();
    let arr = json.as_array().unwrap();
    assert_eq!(arr.len(), 2);
    // Identities are listed sorted by name: `personal` before `work`.
    assert_eq!(arr[0]["name"], "personal");
    assert_eq!(arr[0]["routes"], serde_json::json!([]));
    assert_eq!(arr[1]["name"], "work");
    assert_eq!(arr[1]["user"]["email"], "jane@work.example");
    assert_eq!(
        arr[1]["routes"],
        serde_json::json!([format!("{}/", t.git_path(&dir))])
    );
}


#[test]
fn init_writes_to_xdg_git_config_when_it_is_the_global_file() {
    let t = TestEnv::new();
    // git's rule: no ~/.gitconfig + existing $XDG_CONFIG_HOME/git/config
    // -> --global writes there.
    let xdg_git = t.mkdirs(".config/git").join("config");
    let original = "[init]\n\tdefaultBranch = main\n";
    fs::write(&xdg_git, original).unwrap();

    t.ok(&["init"]);

    assert!(!t.gitconfig().exists(), "~/.gitconfig must not be created");
    let after = t.read(&xdg_git);
    assert!(after.contains("defaultBranch"));
    assert!(after.contains("routes.gitconfig"));
    let backups = t.backups_of(&xdg_git);
    assert_eq!(backups.len(), 1);
    assert_eq!(t.read(&backups[0]), original);
}

#[test]
fn init_honors_git_config_global_override() {
    let t = TestEnv::new();
    // Point GIT_CONFIG_GLOBAL at a non-default file: `git config --global`
    // writes there, and git-id must back up and target that same file.
    let custom = t.home.join("custom-global.gitconfig");
    fs::write(&custom, "# preexisting global\n").unwrap();

    t.cmd()
        .env("GIT_CONFIG_GLOBAL", &custom)
        .args(["init", "--no-use-config-only"])
        .assert()
        .success();

    let after = t.read(&custom);
    assert!(after.contains("# preexisting global"), "content preserved");
    assert!(
        after.contains("routes.gitconfig"),
        "include added to the override"
    );
    assert_eq!(
        t.backups_of(&custom).len(),
        1,
        "the override file must be backed up"
    );
    assert!(
        !t.gitconfig().exists(),
        "~/.gitconfig must not be touched when GIT_CONFIG_GLOBAL is set"
    );
}


#[test]
fn init_preserves_existing_gitconfig_and_backs_it_up() {
    let t = TestEnv::new();
    let original = "# my precious config\n[alias]\n\tst = status\n";
    fs::write(t.gitconfig(), original).unwrap();

    t.ok(&["init"]);

    let after = t.read(&t.gitconfig());
    assert!(after.contains("# my precious config"));
    assert!(after.contains("st = status"));
    assert!(after.contains("routes.gitconfig"));

    let backups = t.backups_of(&t.gitconfig());
    assert_eq!(backups.len(), 1, "exactly one timestamped backup expected");
    assert_eq!(
        t.read(&backups[0]),
        original,
        "backup must be byte-identical"
    );

    // Re-running without changes must not create another backup.
    t.ok(&["init"]);
    assert_eq!(t.backups_of(&t.gitconfig()).len(), 1);
}

